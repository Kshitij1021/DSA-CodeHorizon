// Two numbers are given a and b, print a - b
#include<iostream>
using namespace std;

int main()
{
    int a,b;                                                // Declaration of the integer type variables
    cout<<"Enter the first number: ";
    cin>>a;                                                 // Taking the input of first number
    cout<<"Enter the second number: ";
    cin>>b;                                                 // Taking the input of second number

    cout<<a<<" - "<<b<<" = "<<a-b;                          // Result
}