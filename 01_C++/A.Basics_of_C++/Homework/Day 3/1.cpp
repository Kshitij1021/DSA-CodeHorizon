// Two numbers are given, print their product
#include<iostream>
using namespace std;

int main()
{
    int a,b;                                                        // Declaration of the integer type variable
    cout<<"Enter the first number: ";
    cin>>a;                                                         // Taking the input of first number
    cout<<"Enter the second number: ";
    cin>>b;                                                         // Taking the input of second number

    cout<<"The product of "<<a<<" and "<<b<<" is: "<<a*b;           // Result
}