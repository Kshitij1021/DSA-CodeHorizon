#include <iostream>
using namespace std;

int main()
{

    int a,b;                                                            //Declaration of integers a and b

    cout<< "Enter the first number: ";
    cin>>a;                                                             //Taking the input value of a

    cout<< "Enter the second number: ";
    cin>>b;                                                             //Taking the input value of b
                                                                        //"<<" is also known as exctraction operator

    cout<< "The sum of the two given numbers is: "<<a+b;                //Giving the sum of a and b
}